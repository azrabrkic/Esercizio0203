package terzoEs;

import java.time.LocalDate;

public class Lavorazione {
	private String descrizione;
	private int costo;
	private LocalDate dataDiRiparazione;
	
	
	
	public String getDescrizione() {
		return descrizione;
	}
	public int getCosto() {
		return costo;
	}
	public LocalDate getDataDiRiparazione() {
		return dataDiRiparazione;
	}
	
	
}
